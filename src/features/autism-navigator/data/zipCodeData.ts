function normalizeCountyName(county: string): string {
  return county
    .trim()
    .replace(/\s+county$/i, '')
    .replace(/\s+/g, ' ');
}

export function getZipCodesByCounty(state: string, county: string): string[] {
  const normalizedCounty = normalizeCountyName(county);
  return zipCodesByCounty[state]?.[county] || zipCodesByCounty[state]?.[normalizedCounty] || [];
}

// Lookup ZIP code to find state and county
export function lookupZipCode(zipCode: string): { state: string; county: string } | null {
  for (const [state, counties] of Object.entries(zipCodesByCounty)) {
    for (const [county, zips] of Object.entries(counties)) {
      if (zips.includes(zipCode)) {
        return { state, county };
      }
    }
  }
  return null;
}
